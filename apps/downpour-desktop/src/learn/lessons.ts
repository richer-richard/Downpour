import type { FingerId } from './keyboardLayout';

export type LessonStep =
  | { kind: 'intro_key'; key: string; finger: FingerId; description: string }
  | { kind: 'drill'; prompt: string; hint?: string }
  | { kind: 'word_drill'; words: string[]; hint?: string }
  | { kind: 'practice'; durationSeconds: number; pool: string[] };

export interface Lesson {
  id: string;
  unit: string;
  order: number;
  title: string;
  summary: string;
  steps: LessonStep[];
  unlockRequires?: string;
}

// ---- helpers ----
function intro(key: string, finger: FingerId, description: string): LessonStep {
  return { kind: 'intro_key', key, finger, description };
}
function drill(prompt: string, hint?: string): LessonStep {
  return { kind: 'drill', prompt, hint };
}
function words(list: string[], hint?: string): LessonStep {
  return { kind: 'word_drill', words: list, hint };
}
function practice(pool: string[], durationSeconds = 60): LessonStep {
  return { kind: 'practice', durationSeconds, pool };
}

interface Builder {
  id: string;
  title: string;
  summary: string;
  steps: LessonStep[];
}

function buildUnit(unit: string, builders: Builder[], startOrder: number): Lesson[] {
  const result: Lesson[] = [];
  let prev: string | undefined;
  builders.forEach((b, i) => {
    result.push({
      id: b.id,
      unit,
      order: startOrder + i,
      title: b.title,
      summary: b.summary,
      steps: b.steps,
      unlockRequires: prev,
    });
    prev = b.id;
  });
  return result;
}

