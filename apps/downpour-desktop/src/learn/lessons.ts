// Cross-unit prerequisites: first lesson of each unit requires the last lesson of the previous unit.
(function linkUnits() {
  const byUnit = new Map<string, Lesson[]>();
  for (const lesson of LESSONS) {
    const list = byUnit.get(lesson.unit) ?? [];
    list.push(lesson);
    byUnit.set(lesson.unit, list);
  }
  const unitOrder = [
    'Home Row',
    'Top Row',
    'Bottom Row',
    'Shift & Capitals',
    'Numbers',
    'Symbols',
    'Speed Building',
  ];
  for (let i = 1; i < unitOrder.length; i += 1) {
    const prevUnit = byUnit.get(unitOrder[i - 1]);
    const currUnit = byUnit.get(unitOrder[i]);
    if (prevUnit && currUnit && prevUnit.length > 0 && currUnit.length > 0) {
      currUnit[0].unlockRequires = prevUnit[prevUnit.length - 1].id;
    }
  }
})();

export function getLesson(id: string): Lesson | undefined {
  return LESSONS.find((lesson) => lesson.id === id);
}

export function isLessonUnlocked(
  lesson: Lesson,
  progress: Record<string, { completed: boolean }>,
): boolean {
  if (!lesson.unlockRequires) {
    return true;
  }
  return Boolean(progress[lesson.unlockRequires]?.completed);
}
